    public void TableManager() {
        try {
            FileReader fr = new FileReader(managerFile);
            BufferedReader br = new BufferedReader(fr);

            DefaultTableModel tblmodel = (DefaultTableModel) tblUser.getModel();
            tblmodel.setRowCount(0);
            Object[] lines = br.lines().toArray();
            for (int i = 0; i < lines.length; i++) {
                String[] row;
                row = lines[i].toString().split("-");
                tblmodel.addRow(row);
            }

        } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(rootPane, "Error occured while writing to the table");
        }
        this.setVisible(true);
    }
