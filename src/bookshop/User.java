    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        try {
            boolean valid = true;

            if (txtUserId.getText().isEmpty()
                    || txtName.getText().isEmpty()
                    || txtUserName.getText().isEmpty() || txtPassword.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Field Can't be Blank",
                        "Registration", JOptionPane.WARNING_MESSAGE);
                valid = false;
            }
            if (valid == true) {
                String UserId = txtUserId.getText();
                String Name = txtName.getText();
                String UserName = txtUserName.getText();
                String Password = txtPassword.getText();

                String Data = null;
                Data = UserId + "-" + Name + "-" + UserName + "-" + Password;

                FileWriter Writer = new FileWriter(managerFile, true);
                BufferedWriter Buffer = new BufferedWriter(Writer);

                if (IdCheck()) {
                    JOptionPane.showMessageDialog(rootPane, "Id is already available", "Registration", JOptionPane.ERROR_MESSAGE);
                } else if (UNameCheckAvailability()) {
                    JOptionPane.showMessageDialog(rootPane, "UserName already taken", "Registration", JOptionPane.ERROR_MESSAGE);
                }
                else if(!txtPassword.getText().equals(txtCnfPass.getText()))
                {
                JOptionPane.showMessageDialog(null, "Password and Confirm Password must match",
                    "Registration",JOptionPane.ERROR_MESSAGE);
                valid = false;
                 }
                else {
                    managerFile.canWrite();
                    Buffer.write(Data);
                    Buffer.newLine();
                    Buffer.close();
                    Buffer.close();

                    JOptionPane.showMessageDialog(rootPane, "New Manager user created successfully!");
                }

            }
        } catch (IOException ep) {
            System.out.println("An error occured Saving values " + ep);
        }

        TableManager();

        Clear();

    }//GEN-LAST:event_btnRegisterActionPerformed

