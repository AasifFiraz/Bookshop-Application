    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

                  try
        {
             boolean valid=true;

            if( txtBookId.getText().isEmpty() ||
            txtBookName.getText().isEmpty()  ||
            txtAuthor.getText().isEmpty() || txtQuantity.getText().isEmpty() || txtPrice.getText().isEmpty() )

        {
            JOptionPane.showMessageDialog(null, "Field Can't be Blank",
                "Book",JOptionPane.WARNING_MESSAGE);
            valid=false;
        }
        if(valid==true){        
        String BookId=txtBookId.getText();
        String BookName=txtBookName.getText();
        String Author=txtAuthor.getText();
        String Category=cmbCategory.getSelectedItem().toString();
        String Quantity=txtQuantity.getText();
        String Price=txtPrice.getText();
      
       String Data = null;
        Data = BookId +"-"+BookName+"-"+Author+"-"+Category+"-"+Quantity+"-"+Price;
    
        FileWriter Writer = new FileWriter(bookFile, true);
        BufferedWriter Buffer = new BufferedWriter(Writer);
        
        if(BookNameCheckAvailability()) 
            {
            JOptionPane.showMessageDialog(rootPane, "This Book has already been saved","Book",JOptionPane.ERROR_MESSAGE);
            }
         else if(IdCheck()) 
            {
            JOptionPane.showMessageDialog(rootPane, "Id is already available","Registration",JOptionPane.ERROR_MESSAGE);
            }
            
        else
        {
            bookFile.canWrite();
            Buffer.write(Data);
            Buffer.newLine();
            Buffer.close();
            Buffer.close();
        
            JOptionPane.showMessageDialog(rootPane, "Successfully wrote to the file");
        }

       }
      }
       catch (IOException ep)
        {
            System.out.println("An error occured Saving values "+ ep);
        }
         
             TableManager();
             Clear();
    }//GEN-LAST:event_btnSaveActionPerformed

