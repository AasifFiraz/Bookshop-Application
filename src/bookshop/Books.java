    private void txtSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSearchActionPerformed

      try
        {
        boolean found=true;
        String[] words=null; //initialize the word Array
        
        FileReader Fr = new FileReader(bookFile);
        BufferedReader Br = new BufferedReader(Fr);
        
        String Findline;
        String Search = txtSearch.getText(); //Input word to be searched
        
        
        Outerloop:
        while((Findline=Br.readLine()) !=null ) //Read content from file
        {
        words = Findline.split("-"); //Split word using [0]=  [1]=  [2]=
        for (String word : words)
        {
            if (word.equals(Search) )
            {
            found = true;
            System.out.println(Findline);
            
            break Outerloop;
            }
        else
         {
         found = false;
         }
        }
     }
        if(found)
        {
        JOptionPane.showMessageDialog(rootPane, "Book Found"+"\n"+Findline);
        }
        else
        {
        JOptionPane.showMessageDialog(rootPane, "Book Not Found");
        }
        Fr.close();
        Br.close();
        }
        catch (Exception e)
        {
        JOptionPane.showMessageDialog(null,"An error occured while saving the values"+ e, "Form",JOptionPane.ERROR_MESSAGE);
        }
               
        
    }//GEN-LAST:event_txtSearchActionPerformed

