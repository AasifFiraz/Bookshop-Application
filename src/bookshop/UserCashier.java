public boolean IdCheck()
      {
          boolean IdFound = false;
         try
        {

        String[] words=null; //initialize the word Array
        
        FileReader Fr = new FileReader(cashierFile);
        BufferedReader Br = new BufferedReader(Fr);
        
        String Findline;


        String Id = txtUserId.getText();
        
        while((Findline=Br.readLine()) !=null ) 
        {
        words = Findline.split("-"); 
        for (String word : words)
        {
            if (word.equals(Id))
            {
            IdFound = true;
            }
     }
        }
        Fr.close();
        Br.close();
        }
        catch (IOException e)
        {
        JOptionPane.showMessageDialog(null,"An error occured while saving the values"+ e, "Form",JOptionPane.ERROR_MESSAGE);
        }
         return IdFound;
        }
     
