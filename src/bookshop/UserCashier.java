    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        try
        {
            boolean valid=true;

            if( txtUserId.getText().isEmpty() ||
                txtName.getText().isEmpty()  ||
                txtUserName.getText().isEmpty() || txtPassword.getText().isEmpty() )

            {
                JOptionPane.showMessageDialog(null, "Field Can't be Blank",
                    "Registration Form",JOptionPane.WARNING_MESSAGE);
                valid=false;
            }
            if(valid==true){
                String UserId=txtUserId.getText();
                String Name=txtName.getText();
                String UserName=txtUserName.getText();
                String Password=txtPassword.getText();

                String Data = null;
                Data = UserId +"-"+Name+"-"+UserName+"-"+Password;

                FileWriter Writer = new FileWriter(cashierFile, true);
                BufferedWriter Buffer = new BufferedWriter(Writer);

                if(IdCheck())
                {
                    JOptionPane.showMessageDialog(rootPane, "Id is already available","Student Form",JOptionPane.ERROR_MESSAGE);
                }
                else if(UNameCheckAvailability())
                {
                    JOptionPane.showMessageDialog(rootPane, "UserName already taken","Users",JOptionPane.ERROR_MESSAGE);
                }
                else if(!txtPassword.getText().equals(txtCnfPass.getText()))
                {
                JOptionPane.showMessageDialog(null, "Password and Confirm Password must match",
                    "Registration",JOptionPane.ERROR_MESSAGE);
                valid = false;
                 }
                else
                {
                    cashierFile.canWrite();
                    Buffer.write(Data);
                    Buffer.newLine();
                    Buffer.close();
                    Buffer.close();

                    JOptionPane.showMessageDialog(rootPane, "Successfully wrote to the file");
                }

            }
        }
        catch (IOException ep)
        {
            System.out.println("An error occured Saving values "+ ep);
        }

        TableManager();
        Clear();

    }//GEN-LAST:event_btnRegisterActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed

                DefaultTableModel tblmodel = (DefaultTableModel)tblUser.getModel();
        
        if(tblUser.getSelectedRowCount() ==1 ){
        
        String Id = txtUserId.getText();
        String Name = txtName.getText();
        String UserName = txtUserName.getText();
        String Pass = txtPassword.getText();
        
       tblmodel.setValueAt(Id, tblUser.getSelectedRow(), 0);
       tblmodel.setValueAt(Name, tblUser.getSelectedRow(), 1);
       tblmodel.setValueAt(UserName, tblUser.getSelectedRow(), 2);
       tblmodel.setValueAt(Pass, tblUser.getSelectedRow(), 3);
       
       //Update Message
       JOptionPane.showMessageDialog(this, "Successfully Edited");
       
        }
        else{
            if(tblUser.getRowCount()==0){
            JOptionPane.showMessageDialog(this, "Input a record to edit");
            }
            else{
            JOptionPane.showMessageDialog(this, "Please select a row to update");    
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

